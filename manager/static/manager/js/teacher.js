function Popup() {
  document.getElementById('add-form').style.visibility = 'visible';
  document.getElementById('add-close-btn').style.visibility = 'visible';
}

function CloseForm() {
  document.getElementById('add-form').style.visibility = 'hidden';
  document.getElementById('add-close-btn').style.visibility = 'hidden';
}

function ShowModification() {
  document.getElementById('modify-form').style.visibility = 'visible';
  document.getElementById('modify-close-btn').style.visibility = 'visible';
}

function CloseModification() {
  document.getElementById('modify-form').style.visibility = 'hidden';
  document.getElementById('modify-close-btn').style.visibility = 'hidden';
}